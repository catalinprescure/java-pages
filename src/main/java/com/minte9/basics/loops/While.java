/**
 * While loop is going ...
 * as long as a boolean condition is true
 */

package com.minte9.basics.loops;

public class While {
    public static void main(String[] args) {
        
        int i = 0;
        while(i<10) {
            
            System.out.println(i); 
                // 0 1 2 ... 9
            i++;
        }
    }
}
